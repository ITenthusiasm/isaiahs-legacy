Cypress.Commands.add("createUser", () => {
  const user = {
    username: faker.internet.userName(),
    password: faker.internet.password(),
  };

  cy.request({
    url: "http://localhost:5000/users",
    method: "POST",
    body: user,
  }).then(({ body }) => ({ ...body }));
});
